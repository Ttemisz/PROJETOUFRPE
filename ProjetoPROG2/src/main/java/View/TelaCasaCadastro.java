    private void BotaoRegistrarcasaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoRegistrarcasaActionPerformed
      
      if (caixacasacadastro1.getText().matches("") || caixacasacadastro2.getText().matches("") || caixacasacadastro3.getText().matches("") || Caixacasaid.getText().matches("")) {
    JOptionPane.showMessageDialog(rootPane, "Todos os campos precisam estar preenchidos");
} else {
    try {
        MenuController cadastro = new MenuController();
        cadastro.cadastroCasas(this);
    } catch (SQLException ex) {
        Logger.getLogger(TelaCasaCadastro.class.getName()).log(Level.SEVERE, null, ex);
    }
    this.setVisible(false);
}

    }//GEN-LAST:event_BotaoRegistrarcasaActionPerformed

